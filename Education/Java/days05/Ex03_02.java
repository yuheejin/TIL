package Education.Java.days05;

public class Ex03_02 {
	public static void main(String[] args) {
		
		for (int i = 'ㅏ'; i <= 'ㅣ'; i++) {
			System.out.printf("%d - [%c]\n", i, (char) i);
		}
		
		for (int i = 'ㄱ'; i <= 'ㅎ'; i++) {
			System.out.printf("%d - [%c]\n", i, (char) i);
		}
		
		for (int i = '가'; i <= '힣'; i++) {
			System.out.printf("%d - [%c]\n", i, (char) i);
		} // for

	} // main
} // class

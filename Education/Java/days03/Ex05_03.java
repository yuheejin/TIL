public class Ex05_03 {

	public static void main(String[] args) {
		
		System.out.println( 10 > 3 ); // 참(true)
		System.out.println( 10 < 3 ); // 거짓(false)
		System.out.println( 10 >= 3 ); // 참(true)
		System.out.println( 10 <= 3 ); // 거짓(false)
		
		// (주의) 같다(==)  다르다(!=)
		System.out.println( 10 == 3 ); // 거짓(false)
		System.out.println( 10 != 3 ); // 참(true)
		
		
		//System.out.println( 10 = 3 ); // 거짓(false)
		//System.out.println( 10 =!3 ); // 참(true)
		

	} // main

} // class





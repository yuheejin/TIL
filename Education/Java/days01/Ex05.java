package days01;

public class Ex05 {

	public static void main(String[] args) {
		
		// 1) + 덧셈연산자
		System.out.println(   3 + 5  );   // 8
		
		// 2) +  문자열연결연산자
		String name = "이창익";
		System.out.println(   "이름 : " + name  );  // "이름 : 이창익"		

		// 3) +  문자열연결연산자
		// "문자열" + 정수
		//  정수 + "문자열" 
		int age= 20;
		System.out.println(   "나이 : " + age  ); // "이름 : 20"
	} // main

} // class

package Education.Java.days01;

public class Ex05 {
	public static void main(String[] args) {
		
		// + 덧셈연산자
		System.out.println( 3+5 );
		
		// + 문자열연결연산자
		System.out.println("이름: " + "유희진");
		String name = "유희진";
		System.out.println("이름: " + name);
		
		
		// "문자열" + 정수 -> 이때 +는 문자열 연결 연산자로 쓰임
		System.out.println("나이: " + 20);
		int age = 20;
		System.out.println("나이: " + age);
		
	} //main
} //class
